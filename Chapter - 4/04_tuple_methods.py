a = (1, 5676, 975, 865, 468, 975, False, "Rohan")
print(a)

#  count(x)
print(a.count(975))

# Index
print(a.index(975))


# Length of tuple
print(len(a))

# Repetition (*)
t = ("Hi",) * 3
print(t) 

# Concatenation (+)
a = (1, 2)
b = (3, 4)
print(a + b)

# Membership (in)
t = (1, 2, 3)
print(2 in t)   # True
print(5 in t)   # False
